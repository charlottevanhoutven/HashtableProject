public class RGB {

    private int R;
    private int G;
    private int B;

    public RGB() {
        this.R = 0;
        this.G = 0;
        this.B = 0;
    }

    public int getR() {
        return this.R;
    }

    public int getG() {
        return this.G;
    }

    public int getB() {
        return this.B;
    }

    public void setR(int r) {
        this.R = r;
    }

    public void setG(int g) {
        this.G = g;
    }

    public void setB(int b) {
        this.B = b;
    }

}
